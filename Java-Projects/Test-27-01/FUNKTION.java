import java.util.Scanner;

class FUNKTION {
  public static void main(String[]args) {
    Scanner scann = new Scanner(System.in);
    System.out.println("Startwert für x eingeben: ");
    int xStartWert = scann.nextInt();
    System.out.println("Endwert für x eingeben: ");
    int xEndWert = scann.nextInt();
    
    for (int i = xStartWert; i < xEndWert; i++) {
      int y = 2 * (i*i) + 5;
      System.out.println("x: " + i);
      System.out.println("y: " + y);
    }
  }
  }
