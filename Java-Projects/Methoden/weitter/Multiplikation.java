class Multiplikation {
  public static void main(String[]args) {
    double ersteWert = 2.36;
    double zweiteWert = 3.21;
    
    System.out.println("1. Wert...: " + ersteWert);
    System.out.println("2. Wert...: " + zweiteWert);
    System.out.println("Ergebniss.: "+ersteWert*zweiteWert);
  }
}
